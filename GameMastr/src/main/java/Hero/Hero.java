package Hero;

public class Hero {
    private Integer id;
    private Integer raceId;
    private Integer cultureId;
    private Integer propertiesId;
    private String Name;
    private Integer age;
    private Integer height;
    private Integer weight;
    private String eye_color;
    private String hair_color;
    private String birthdate;
    private String title;
    private Integer social;
    private String profession;
    private String secondProfession;
    private Integer talentsId;
    private Integer vitalSetId;

    public Hero(){

    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getRaceId() {
        return raceId;
    }

    public void setRaceId(Integer raceId) {
        this.raceId = raceId;
    }

    public Integer getCultureId() {
        return cultureId;
    }

    public void setCultureId(Integer cultureId) {
        this.cultureId = cultureId;
    }

    public Integer getPropertiesId() {
        return propertiesId;
    }

    public void setPropertiesId(Integer propertiesId) {
        this.propertiesId = propertiesId;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Integer getHeight() {
        return height;
    }

    public void setHeight(Integer height) {
        this.height = height;
    }

    public Integer getWeight() {
        return weight;
    }

    public void setWeight(Integer weight) {
        this.weight = weight;
    }

    public String getEye_color() {
        return eye_color;
    }

    public void setEye_color(String eye_color) {
        this.eye_color = eye_color;
    }

    public String getHair_color() {
        return hair_color;
    }

    public void setHair_color(String hair_color) {
        this.hair_color = hair_color;
    }

    public String getBirthdate() {
        return birthdate;
    }

    public void setBirthdate(String birthdate) {
        this.birthdate = birthdate;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Integer getSocial() {
        return social;
    }

    public void setSocial(Integer social) {
        this.social = social;
    }

    public String getProfession() {
        return profession;
    }

    public void setProfession(String profession) {
        this.profession = profession;
    }

    public String getSecondProfession() {
        return secondProfession;
    }

    public void setSecondProfession(String secondProfession) {
        this.secondProfession = secondProfession;
    }

    public Integer getTalentsId() {
        return talentsId;
    }

    public void setTalentsId(Integer talentsId) {
        this.talentsId = talentsId;
    }

    public Integer getVitalSetId() {
        return vitalSetId;
    }

    public void setVitalSetId(Integer vitalSetId) {
        this.vitalSetId = vitalSetId;
    }
}
